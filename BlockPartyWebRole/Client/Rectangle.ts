class Rectangle {
    public X: number = 0;
    public Y: number = 0;
    public Width: number = 0;
    public Height: number = 0;

    constructor(x: number, y: number, width: number, height: number) {
        this.X = x;
        this.Y = y;
        this.Width = width;
        this.Height = height;
    }
}